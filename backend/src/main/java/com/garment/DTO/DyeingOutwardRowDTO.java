package com.garment.DTO;

public class DyeingOutwardRowDTO {
    private String lotNo;
    private String fabricName;
    private String shade;
    private String mcSize;
    private String greyGSM;
    private String regdSize;
    private String processing;
    private String roll;
    private String weight;
    private String knittingYarnRate;
    private String rate;
    private String amount;

    // getters and setters
    public String getLotNo() { return lotNo; }
    public void setLotNo(String lotNo) { this.lotNo = lotNo; }
    public String getFabricName() { return fabricName; }
    public void setFabricName(String fabricName) { this.fabricName = fabricName; }
    public String getShade() { return shade; }
    public void setShade(String shade) { this.shade = shade; }
    public String getMcSize() { return mcSize; }
    public void setMcSize(String mcSize) { this.mcSize = mcSize; }
    public String getGreyGSM() { return greyGSM; }
    public void setGreyGSM(String greyGSM) { this.greyGSM = greyGSM; }
    public String getRegdSize() { return regdSize; }
    public void setRegdSize(String regdSize) { this.regdSize = regdSize; }
    public String getProcessing() { return processing; }
    public void setProcessing(String processing) { this.processing = processing; }
    public String getRoll() { return roll; }
    public void setRoll(String roll) { this.roll = roll; }
    public String getWeight() { return weight; }
    public void setWeight(String weight) { this.weight = weight; }
    public String getKnittingYarnRate() { return knittingYarnRate; }
    public void setKnittingYarnRate(String knittingYarnRate) { this.knittingYarnRate = knittingYarnRate; }
    public String getRate() { return rate; }
    public void setRate(String rate) { this.rate = rate; }
    public String getAmount() { return amount; }
    public void setAmount(String amount) { this.amount = amount; }
}
